#include<iostream>
using namespace std;

int main(){
    //integer datatype
    int integerValue = 20;
cout<<"Stored integer value is: "<< integerValue<<endl;

//Floating point datatype
float floatValue= 3;
cout<<"Stored float value is: "<< floatValue<<endl;

//Character datatype
char charValue = 'P';
cout<<"Stored char value is: -"<< charValue<<endl;

//Boolean datatype
bool boolValue = true;
cout<<"Stored boolean value is: -"<< boolValue<<endl;
    return 0;

}